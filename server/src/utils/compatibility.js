// ABO + Rh compatibility map
export const COMPATIBLE = {
  "O-": ["O-"],
  "O+": ["O-", "O+"],
  "A-": ["O-", "A-"],
  "A+": ["O-", "O+", "A-", "A+"],
  "B-": ["O-", "B-"],
  "B+": ["O-", "O+", "B-", "B+"],
  "AB-": ["O-", "A-", "B-", "AB-"],
  "AB+": ["O-", "O+", "A-", "A+", "B-", "B+", "AB-", "AB+"]
};

// Function to get compatible donor groups for a recipient
export function compatibleDonorGroups(recipientGroup) {
  return COMPATIBLE[recipientGroup] || [];
}
